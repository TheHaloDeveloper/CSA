public class Main {
    public static void main(String[] args) {
        for (int i = 2; i <= 50; i += 2) {
            System.out.print(i + " ");
        }

        System.out.println();
        int i = 2;
        while (i <= 50) {
            System.out.print(i + " ");
            i += 2;
        }
    }
}