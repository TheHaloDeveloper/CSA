public class Main {
    public static void main(String[] args) {
        int number = 75;

        if (number > 50 && number % 5 == 0) {
            System.out.println("The number is greater than 50 and divisible by 5.");
        } else {
            System.out.println("The number is not greater than 50 and divisible by 5.");
        }
    }
}